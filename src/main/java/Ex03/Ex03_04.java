package Ex03;

public class Ex03_04 {

    public static void main(String[] args) {

        // 컴퓨터가 2진수로 처리하기 때문에 ? 뒤에 계산하는 과정에서 뒷자리가 남음.
        System.out.printf("%d / %d = %f \n", 100, 200, 0.5); // 정수 ( %d ) 와 실수 ( %s ) 1개를 출력한다.

        System.out.printf("%c %c \n", 'a', 'K'); // "문자" 2개를 출력한다.
        System.out.printf("%s %s \n", "IT CookBook", "JAVA"); // "문자열" 2개를 출력한다.
    }
}
