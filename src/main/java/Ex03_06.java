public class Ex03_06 {

    public static void main(String[] args) {

        System.out.printf("\n줄바꿈\n연습\n"); // \n 는 줄바꿈 ( Enter 키를 누른 효과 )
        System.out.printf("\t탭키\t연습 \n"); // \t 는 들여쓰기 ( Tab 키를 누른 효과 )
        System.out.printf("이것을\r덮어씁니다. \n"); // \r 는 줄의 맨 앞으로 이동 ( Home 키를 누른 효과 )
        System.out.printf("글자가\"강조\"되는 효과 \n"); // \" 는 강조를 해서 출력할 수 있음 ( "를 출력해줌 )
        System.out.printf("\\\\\\ 역슬래시 세개 출력 \n"); // \\ 는 \ 출력 ( 역슬래시를 출력해줌 )
    }
}
